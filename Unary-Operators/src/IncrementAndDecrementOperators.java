public class IncrementAndDecrementOperators {
    public static void main(String[] args) {

        int myInt = 5;

        System.out.println(myInt++);
        System.out.println(myInt);

        System.out.println(++myInt);
        System.out.println(myInt);

        System.out.println(myInt--);
        System.out.println(myInt);

        System.out.println(--myInt);
        System.out.println(myInt);

    }
}
