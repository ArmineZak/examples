public class ArithmeticOperators {
    public static void main(String[] args) {

        double num1 = 12.5, num2 = 3.5, result;

        result = num1 + num2;
        System.out.println("num1 + num2 = " + result);

        result = num1 - num2;
        System.out.println("num1 - num2 = " + result);

        result = num1 * num2;
        System.out.println("num1 * num2 = " + result);

        result = num1 / num2;
        System.out.println("num1 / num2 = " + result);

        result = num1 % num2;
        System.out.println("num1 % num2 = " + result);

    }
}
